package com.example.atta.furnitureapplication;

class Furniture_Model {


    String id;
    String item_image;
    String item_name;
    String item_price;
    String phone_number;
    String place_date;
    String order_date;
    String total_price;
    int alarm_blink_day;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    public String getItem_image() {
        return item_image;
    }

    public void setItem_image(String item_image) {
        this.item_image = item_image;
    }

    public String getItem_name() {
        return item_name;
    }

    public void setItem_name(String item_name) {
        this.item_name = item_name;
    }

    public String getItem_price() {
        return item_price;
    }

    public void setItem_price(String item_price) {
        this.item_price = item_price;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getPlace_date() {
        return place_date;
    }

    public void setPlace_date(String place_date) {
        this.place_date = place_date;
    }

    public String getOrder_date() {
        return order_date;
    }

    public void setOrder_date(String order_date) {
        this.order_date = order_date;
    }

    public String getTotal_price() {
        return total_price;
    }

    public void setTotal_price(String total_price) {
        this.total_price = total_price;
    }


    public int getAlarm_blink_day() {
        return alarm_blink_day;
    }

    public void setAlarm_blink_day(int alarm_blink_day) {
        this.alarm_blink_day = alarm_blink_day;
    }

}
